#include <string.h>

void	ft_striter(char *s, void (*f)(char *))
{
	int pos;

	if (f == NULL || s == NULL)
		return ;
	pos = 0;
	while (s[pos])
	{
		f(&(s[pos]));
		pos++;
	}
}
