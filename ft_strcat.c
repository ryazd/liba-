char	*ft_strcat(char *s1, const char *s2)
{
	int pos;
	int pos2;

	pos2 = 0;
	pos = 0;
	while (s1[pos])
		pos++;
	while (s2[pos2])
	{
		s1[pos] = s2[pos2];
		pos++;
		pos2++;
	}
	s1[pos] = '\0';
	return (s1);
}
